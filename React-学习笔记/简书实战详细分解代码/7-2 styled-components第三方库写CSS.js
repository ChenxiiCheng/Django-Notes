//使用styled-components第三方库写组件css
根目录下的index.js是整个项目入口，我们在里面挂载了App这个组件，然后我们在App这个组件里挂载Header组件
在src/common/header/index.js中写我们的header组件

我们通过styled-components这个第三方库，可以定义一个组件，然后在同个目录下的style.js中写这个组件的css
eg:
在index.js中：
import {
  HeaderWrapper,
  Logo,
  Nav,
  NavItem,
  NavSearch,
  Addition,
  Button
} from './style';

class Header extends Component {
  render() {
    return (
      <HeaderWrapper>
        <Logo />
        <Nav>
          <NavItem className="left active">首页</NavItem>
          <NavItem className="left">下载App</NavItem>
          <NavItem className="right">登陆</NavItem>
          <NavItem className="right">Aa</NavItem>
          <NavSearch />
        </Nav>
        <Addition>
          <Button className="writting">写文章</Button>
          <Button className="reg">注册</Button>
        </Addition>
      </HeaderWrapper>
    );
  }
}

export default Header;



